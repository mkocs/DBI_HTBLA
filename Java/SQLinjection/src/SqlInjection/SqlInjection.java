    private void jButton_exec_sqlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_exec_sqlActionPerformed
        Connection conn = null;
        String connStr;
        Statement stmt;
        ResultSet rs;
        String sql;
        String eingabe;

        try {
            connStr = this.makeConnectionString();
            this.jTextArea_error.setText("");
            this.jLabel_connString.setText(connStr);
            eingabe = this.jTextArea_ename.getText();

            conn = DriverManager.getConnection(connStr, "scott", "scott");
 
            // einfaches Statement
            stmt = conn.createStatement();
            sql = "SELECT empno, ename, job, mgr, sal, COALESCE(comm,3) \n"
                + "FROM emp WHERE deptno = 20 and ename like '" + eingabe + "'";

            rs = stmt.executeQuery(sql);

            ausgeben(rs);

            this.jTextArea_select.setText(sql);
            System.out.println("SQL:" + sql);

        } catch (SQLException ex) {
            this.jTextArea_error.setText(
                    "SQLException: " + ex.getMessage()
                    +"SQLState: " + ex.getSQLState()+"\n"
                    +"VendorError: " + ex.getErrorCode());
            // handle any errors
            System.out.println("SQLException: " + ex.getMessage());
            System.out.println("SQLState: " + ex.getSQLState());
            System.out.println("VendorError: " + ex.getErrorCode());
        }

    }//GEN-LAST:event_jButton_exec_sqlActionPerformed

