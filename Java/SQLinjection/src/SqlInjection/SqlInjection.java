    public String makeConnectionString() {
            String host = this.jTextField_host.getText();
            String connStr = null;
            
            // Mit MySql verbinden
            if (this.do_mysql) {
                connStr = "jdbc:mysql://" + host + "/scott";
                // z.B jdbc:mysql://mysql.htl-donaustadt.at/scott
            }
            // Mit oracle verbinden
            if (this.do_oracle) {
                connStr = "jdbc:oracle:thin:@" + host + ":1521:XE";
                // jdbc:oracle:thin:@oracledb.htl-donaustadt.at:1521:XE
            }
        return connStr;
    }
    
    public void ausgeben(ResultSet rs) throws SQLException {

        String str;
        ArrayList list = new ArrayList();
        str = "";
        for (int i = 1; i <= rs.getMetaData().getColumnCount(); i++) {
            if (i > 1) {
                str += ", ";
            }
            str += rs.getMetaData().getColumnLabel(i);
        }
        System.out.println(str);

        DefaultTableModel model = (DefaultTableModel) this.jTable_select.getModel();
        model.setRowCount(0);

        rs.getMetaData().getColumnCount();
        while (rs.next()) {
            str = "";
            list.clear();
            for (int i = 1; i <= rs.getMetaData().getColumnCount(); i++) {
                if (i > 1) {
                    str += ", ";
                }
                str += rs.getString(i);
                list.add(rs.getString(i));
            }

            System.out.println(str);
            model.addRow(list.toArray());
        }
    }

